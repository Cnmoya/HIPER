import Vida
import Perseverancia
import Curiosidad
import Maestria
import Estrellas


#Ingredientes basicos para alcanzar las estrellas:


Vida.aprendizaje = Curiosidad * Perseverancia

#Hambre de conocimiento


while not Vida.exito():
    Vida.intentar()

#Seguir intentando

if Maestria.alcanzada == True:
    Vida.descansar()

#Alcanzar la maestria

Estrellas.alcanzadas = True



